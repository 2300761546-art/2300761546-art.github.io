// ─────────────────────────────────────────────
//  开始难度练习
// ─────────────────────────────────────────────
function startDifficulty(diff) {
  const allInDiff = QUESTIONS.filter(q => q.d === diff);
  const practiced = store.practiced[diff];

  // 未练习过的题目
  let unpracticed = allInDiff.filter(q => !practiced.includes(q.id));

  // 若未练习的题目不足10道，说明快练完了，重置该难度记录重新开始
  if (unpracticed.length < 1) {
    store.practiced[diff] = [];
    saveStore();
    unpracticed = allInDiff;
    showToast('已完成所有题目，重新开始！');
  }

  // 随机抽取最多10道
  const pool = shuffle([...unpracticed]);
  const selected = pool.slice(0, Math.min(10, pool.length));

  state.quizMode = diff;
  state.quizQueue = selected;
  state.quizIndex = 0;
  state.score = 0;
  state.sessionWrongIds = [];
  state.sessionCorrectIds = [];
  startQuiz();
}

// ─────────────────────────────────────────────
//  开始错题本
// ─────────────────────────────────────────────
function startWrongBank() {
  if (store.wrongBank.length === 0) {
    showToast('错题本是空的，继续加油！');
    return;
  }
  const questions = QUESTIONS.filter(q => store.wrongBank.includes(q.id));
  if (questions.length === 0) {
    showToast('错题本是空的，继续加油！');
    return;
  }
  state.quizMode = 'wrong';
  state.quizQueue = shuffle([...questions]);
  state.quizIndex = 0;
  state.score = 0;
  state.sessionWrongIds = [];
  startQuiz();
}

// ─────────────────────────────────────────────
//  开始复习
// ─────────────────────────────────────────────
function startReview() {
  if (store.reviewBank.length === 0) {
    showToast('复习本是空的，先去做题积累吧！');
    return;
  }
  const questions = QUESTIONS.filter(q => store.reviewBank.includes(q.id));
  if (questions.length === 0) {
    showToast('复习本是空的，先去做题积累吧！');
    return;
  }
  state.quizMode = 'review';
  state.quizQueue = shuffle([...questions]);
  state.quizIndex = 0;
  state.score = 0;
  state.sessionWrongIds = [];
  startQuiz();
}

// ─────────────────────────────────────────────
//  进入答题界面
// ─────────────────────────────────────────────
function startQuiz() {
  // 设置右上角状态框
  const labelEl = document.getElementById('quiz-mode-label');
  const scoreEl = document.getElementById('score-value');
  const exitBtn = document.getElementById('quiz-exit-btn');

  if (state.quizMode === 'wrong') {
    labelEl.textContent = '错题本';
    scoreEl.style.display = 'none';
    exitBtn.classList.remove('hidden');
  } else if (state.quizMode === 'review') {
    labelEl.textContent = '复习';
    scoreEl.style.display = 'none';
    exitBtn.classList.remove('hidden');
  } else {
    labelEl.textContent = '分数';
    scoreEl.style.display = '';
    scoreEl.textContent = '0';
    exitBtn.classList.add('hidden');
  }

  showScreen('quiz');
  renderQuestion();
}

// ─────────────────────────────────────────────
//  渲染当前题目
// ─────────────────────────────────────────────
function renderQuestion() {
  const q = state.quizQueue[state.quizIndex];
  const total = state.quizQueue.length;
  const current = state.quizIndex + 1;

  // 进度
  document.getElementById('question-progress').textContent = current + ' / ' + total;

  // 题目文字
  document.getElementById('question-text').textContent = q.s;

  // 选项
  const container = document.getElementById('options-list');
  container.innerHTML = '';
  const letters = ['A', 'B', 'C', 'D'];
  q.o.forEach((optText, idx) => {
    const item = document.createElement('div');
    item.className = 'option-item';
    item.dataset.idx = idx;
    item.innerHTML = `
      <span class="option-letter">${letters[idx]}</span>
      <span class="option-text">${optText}</span>
      <img class="option-check" src="./美术资源/答题界面/绿色对钩.png" alt="✓">
    `;
    item.addEventListener('click', () => handleAnswer(idx));
    container.appendChild(item);
  });

  // 隐藏解析面板
  const panel = document.getElementById('analysis-panel');
  panel.classList.add('hidden');

  state.answered = false;

  // 最后一题的按钮文字
  const isLast = state.quizIndex === total - 1;
  const nextBtn = document.getElementById('next-btn');
  if (isLast) {
    if (state.quizMode === 'wrong' || state.quizMode === 'review') {
      nextBtn.textContent = '完成';
    } else {
      nextBtn.textContent = '查看结果';
    }
  } else {
    nextBtn.textContent = '下一题';
  }
}

// ─────────────────────────────────────────────
//  处理答题
// ─────────────────────────────────────────────
function handleAnswer(selectedIdx) {
  if (state.answered) return;
  state.answered = true;

  const q = state.quizQueue[state.quizIndex];
  const isCorrect = selectedIdx === q.c;

  // 更新分数（仅难度模式）
  if (isCorrect && ['easy','medium','hard'].includes(state.quizMode)) {
    state.score += 10;
    const scoreEl = document.getElementById('score-value');
    scoreEl.textContent = state.score;
    scoreEl.classList.remove('bump');
    void scoreEl.offsetWidth; // reflow
    scoreEl.classList.add('bump');
  }

  // 渲染选项颜色
  const items = document.querySelectorAll('.option-item');
  items.forEach(item => {
    item.classList.add('disabled');
    const idx = parseInt(item.dataset.idx);
    if (idx === q.c) {
      item.classList.add('correct');
    } else if (idx === selectedIdx && !isCorrect) {
      item.classList.add('wrong');
    } else {
      item.classList.add('wrong-unselected');
    }
  });

  // 错误答题逻辑
  if (!isCorrect) {
    state.sessionWrongIds.push(q.id);
  } else if (['easy','medium','hard'].includes(state.quizMode)) {
    // 难度模式答对 → 候选加入复习本
    state.sessionCorrectIds.push(q.id);
  }

  // 更新题库状态
  updateBanks(q.id, isCorrect);

  // 显示解析
  showAnalysis(q);
}

// ─────────────────────────────────────────────
//  更新错题本/复习本
// ─────────────────────────────────────────────
function updateBanks(qid, isCorrect) {
  const mode = state.quizMode;

  if (mode === 'wrong') {
    if (isCorrect) {
      // 从错题本移到复习本
      store.wrongBank = store.wrongBank.filter(id => id !== qid);
      if (!store.reviewBank.includes(qid)) store.reviewBank.push(qid);
      saveStore();
    }
    // 答错了：保持在错题本，无需操作
  } else if (mode === 'review') {
    if (!isCorrect) {
      // 从复习本移回错题本
      store.reviewBank = store.reviewBank.filter(id => id !== qid);
      if (!store.wrongBank.includes(qid)) store.wrongBank.push(qid);
      saveStore();
    }
    // 答对了：保持在复习本，无需操作
  }
  // 难度模式的错题收录在 finishQuiz() 中统一处理
}

// ─────────────────────────────────────────────
