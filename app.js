// ─────────────────────────────────────────────
//  应用状态
// ─────────────────────────────────────────────
let state = {
  currentScreen: 'home',
  quizMode: null,       // 'easy'|'medium'|'hard'|'wrong'|'review'
  quizQueue: [],        // 当前队列的题目数组
  quizIndex: 0,
  score: 0,
  answered: false,
  sessionWrongIds: [],   // 本次答题中答错的题目id（仅难度模式用）
  sessionCorrectIds: [], // 本次答题中答对的题目id（仅难度模式用）
};

// localStorage 持久化数据
let store = {
  practiced: { easy: [], medium: [], hard: [] },
  wrongBank: [],   // 题目id数组
  reviewBank: [],  // 题目id数组
};

// ─────────────────────────────────────────────
//  持久化读写
// ─────────────────────────────────────────────
function loadStore() {
  try {
    const raw = localStorage.getItem('liangci_store');
    if (raw) {
      const parsed = JSON.parse(raw);
      store = Object.assign(store, parsed);
      // 兼容旧数据结构
      if (!store.practiced) store.practiced = { easy: [], medium: [], hard: [] };
      ['easy','medium','hard'].forEach(d => {
        if (!store.practiced[d]) store.practiced[d] = [];
      });
      if (!store.wrongBank) store.wrongBank = [];
      if (!store.reviewBank) store.reviewBank = [];
    }
  } catch(e) { /* ignore */ }
}

function saveStore() {
  try {
    localStorage.setItem('liangci_store', JSON.stringify(store));
  } catch(e) { /* ignore */ }
}

