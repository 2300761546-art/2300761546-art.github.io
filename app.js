// ─────────────────────────────────────────────
//  下一题 / 完成
// ─────────────────────────────────────────────
function nextQuestion() {
  const total = state.quizQueue.length;
  const isLast = state.quizIndex === total - 1;

  if (isLast) {
    finishQuiz();
    return;
  }

  state.quizIndex++;
  renderQuestion();
  // 滚回顶部
  document.getElementById('screen-quiz').scrollTo({ top: 0, behavior: 'smooth' });
}

// ─────────────────────────────────────────────
//  完成答题
// ─────────────────────────────────────────────
function finishQuiz() {
  const mode = state.quizMode;

  if (mode === 'wrong' || mode === 'review') {
    // 练习完成界面
    const modeLabel = mode === 'wrong' ? '错题本' : '复习';
    document.getElementById('done-title').textContent = '练习完成！';
    document.getElementById('done-sub').textContent =
      `${modeLabel}练习结束\n共 ${state.quizQueue.length} 道题`;
    showScreen('done');
    return;
  }

  // 难度模式：统一收录错题
  state.sessionWrongIds.forEach(id => {
    if (!store.wrongBank.includes(id)) store.wrongBank.push(id);
  });

  // 难度模式：答对的题目加入复习本
  state.sessionCorrectIds.forEach(id => {
    if (!store.reviewBank.includes(id)) store.reviewBank.push(id);
  });

  // 标记已练习
  const diff = mode;
  state.quizQueue.forEach(q => {
    if (!store.practiced[diff].includes(q.id)) {
      store.practiced[diff].push(q.id);
    }
  });

  saveStore();

  // 结算界面
  showResult(state.score);
}

